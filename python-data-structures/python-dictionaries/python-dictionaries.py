# PYTHON DICTIONARIES

"""
------------------------------------
Python Data Structures: Dictionaries
------------------------------------
1. Python has 4 built-in data types used to store collections of data. This section will focus on "Dictionaries".
2. What is a Dictionary? A "Dictionary" is a collection of key-value pairs, where each key is unique. (Similar to an object in JavaScript)
3. The dictionary items are unordered and mutable.
4. Defined using curly braces {}.
5. SYNTAX:
dictionary_name = {
    key1: value1,
    key2: value2,
}

"""

# EXAMPLE: DICTIONARY HOLDING THE RELATED DETAILS OF A PERSON

person = {
    "name": "Alice",
    "age": 28,
    "city": "Tokyo",
}

print(person) #Expected Output: {'name': 'Alice', 'age': 28, 'city': 'Tokyo'}